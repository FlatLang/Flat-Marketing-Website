<div anchor-button id="xunit">
	<h1>XUNIT TESTING</h1>
	<p>
		<a href="https://en.wikipedia.org/wiki/XUnit">xUnit</a> testing is a common paradigm of unit
		testing that was birthed from
		<a target="_blank" href="https://en.wikipedia.org/wiki/Smalltalk">Smalltalk's</a>
		<a target="_blank" href="https://en.wikipedia.org/wiki/SUnit">SUnit</a>. xUnit testing revolves
		around a few common testing structures: test runners, test cases, test fixtures, and test
		suites.
	</p>
	<ul>
		<li><b>Test runners</b>: program that runs xUnit tests and reports the results</li>
		<li><b>Test cases</b>: elemental case in which suites are built with</li>
		<li><b>Test fixtures</b>: a state needed to run a test</li>
		<li><b>Test suites</b>: set of tests that share same fixture</li>
	</ul>
</div>

